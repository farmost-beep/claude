    def title_page(self, main, sub=''):
        self.add_page()
        self.ln(30)
        self.set_font('H', '', 28)
        self.set_text_color(0x17, 0x2B, 0x4F)
        self.multi_cell(self.w - 2 * self.m, 12, main, align='C')
        if sub:
            self.ln(6)
            self.set_font('S', '', 12)
            self.set_text_color(0x66, 0x66, 0x66)
            self.multi_cell(self.w - 2 * self.m, 7, sub, align='C')
        self.ln(12)
        self.set_draw_color(0x00, 0x96, 0x53)
        self.set_line_width(0.6)
        cx = self.w / 2
        self.line(cx - 25, self.get_y(), cx + 25, self.get_y())
        self.ln(8)
        self.set_font('S', '', 10)
        self.set_text_color(0x99, 0x99, 0x99)
        self.multi_cell(self.w - 2 * self.m, 6, '规划周期：2026年5月 — 2031年5月 | 49岁→54岁', align='C')

    def ch_title(self, t):
        self.set_font('H', '', 18)
        self.set_text_color(0x17, 0x2B, 0x4F)
        self.set_x(self.m)
        self.cell(self.w - 2 * self.m, 10, t)
        self.ln(12)
        self.set_draw_color(0x00, 0x96, 0x53)
        self.set_line_width(0.5)
        self.line(self.m, self.get_y() - 6, self.m + 40, self.get_y() - 6)
        self.ln(6)

    def sub_title(self, t):
        self.set_font('H', '', 13)
        self.set_text_color(0x00, 0x96, 0x53)
        self.set_x(self.m)
        self.cell(self.w - 2 * self.m, 8, t)
        self.ln(10)

    def body(self, t):
        self.set_font('S', '', 10.5)
        self.set_text_color(0x33, 0x33, 0x33)
        self.set_x(self.m)
        self.multi_cell(self.w - 2 * self.m, 6.2, t, align='J')
        self.ln(2)

    def bullet(self, t):
        self.set_font('S', '', 10.5)
        self.set_text_color(0x33, 0x33, 0x33)
        self.set_x(self.m)
        self.cell(6, 6, '>')
        self.multi_cell(self.w - 2 * self.m - 6, 6.2, t, align='J')
        self.set_x(self.m)
        self.ln(1)

    def green_bar(self):
        self.set_draw_color(0x00, 0x96, 0x53)
        self.set_line_width(0.3)
        y = self.get_y()
        self.line(self.m, y, self.w - self.m, y)
        self.ln(4)

    def page_num(self):
        self.set_font('S', '', 8)
        self.set_text_color(0xAA, 0xAA, 0xAA)
        self.set_y(-15)
        self.cell(self.w - 2 * self.m, 5, str(self.page_no()), align='R')


