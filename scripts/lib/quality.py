def check_output(domain: Domain, grade: Grade) -> list[str]:
    """返回该领域+等级的检查项列表。"""
    return QUALITY_CHECKS.get(domain, {}).get(grade, [])


def build_scoring_matrix(domain: Domain) -> str:
    """为该领域生成评分矩阵（Markdown格式），用于交叉审查。"""
    checks = QUALITY_CHECKS.get(domain, {})
    highest = checks.get(Grade.S, checks.get(Grade.A, []))

    if not highest:
        return "暂无评分标准"

    rows = []
    n = len(highest)
    weight = 100 // n
    for i, (dim, desc) in enumerate(highest):
        rows.append(f"| {dim} | {weight}% | {desc} | 不符合 | |")

    header = f"### {domain.value}领域 评分矩阵\n\n"
    header += "| 维度 | 权重 | 10分标准 | 1分标准 | 得分 |\n"
    header += "|:---|:---:|:---|:---|:---:|\n"
    return header + "\n".join(rows) + "\n| **加权总分** | | | | /10 |"
